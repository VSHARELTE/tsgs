        to file a complaint.<br />
        If you are based in Virginia, please visit this
        <a
          href="https://www.oag.state.va.us/consumer-protection/index.php/file-a-complaint"
          >website</a
        >
        to file a complaint.<br />
        If you are based in Connecticut, please visit this
        <a href="https://portal.ct.gov/AG/Common/Complaint-Form-Landing-page"
          >website</a
        > to file a complaint.
      </p>
    </li>
    <li>
      <h4 class="text-xl font-bold">
        File a Complaint (GDPR Article 77, LGPD, POPIA)
      </h4>
      You have the right to bring a claim before their competent data protection
      authority.

      <p>
