      </h4>
      You have the right to have incomplete or inaccurate Personal Data that we process
      about you rectified.
    </li>
    <li>
      <h4 class="text-xl font-bold">
        Right to be Forgotten (right to erasure) (GDPR Article 17, CCPA/CPRA,
        CPA, VCDPA, CTDPA, UCPA, LGPD, POPIA)
      </h4>
      You have the right to request that we delete Personal Data that we process
      about you, unless we need to retain such data in order to comply with a legal
      obligation or to establish, exercise or defend legal claims.
    </li>
    <li>
      <h4 class="text-xl font-bold">
        Right to Restriction of Processing (GDPR Article 18, LGPD)
      </h4>
      You have the right to restrict our processing of your Personal Data under certain
      circumstances. In this case, we will not process your Data for any purpose
      other than storing it.
    </li>
    <li>
      <h4 class="text-xl font-bold">
        Right to Portability (PIPEDA, GDPR Article 20, LGPD)
      </h4>
      You have the right to obtain Personal Data we hold about you, in a structured,
      electronic format, and to transmit such Personal Data to another data controller,
      where this is (a) Personal Data which you have provided to us, and (b) if we
      are processing that data on the basis of your consent or to perform a contract
      with you or the third party that subscribes to services.
    </li>
    <li>
      <h4 class="text-xl font-bold">
        Right to Opt Out (CPRA, CPA, VCDPA, CTDPA, UCPA)
      </h4>
      You have the right to opt out of the processing of your Personal Data for purposes
      of: (1) Targeted advertising; (2) The sale of Personal Data; and/or (3) Profiling
      in furtherance of decisions that produce legal or similarly significant effects
      concerning you. Under CPRA, you have the right to opt out of the sharing of
      your Personal Data to third parties and our use and disclosure of your Sensitive
      Personal Data to uses necessary to provide the products and services reasonably
      expected by you.
    </li>
    <li>
      <h4 class="text-xl font-bold">
        Right to Objection (GDPR Article 21, LGPD, POPIA)
      </h4>
      Where the legal justification for our processing of your Personal Data is our
      legitimate interest, you have the right to object to such processing on grounds
      relating to your particular situation. We will abide by your request unless
      we have compelling legitimate grounds for processing which override your interests
      and rights, or if we need to continue to process the Personal Data for the
      establishment, exercise or defense of a legal claim.
    </li>
    <li>
      <h4 class="text-xl font-bold">
        Nondiscrimination and nonretaliation (CCPA/CPRA, CPA, VCDPA, CTDPA,
        UCPA)
      </h4>
      You have the right not to be denied service or have an altered experience for
      exercising your rights.
    </li>
    <li>
      <h4 class="text-xl font-bold">File an Appeal (CPA, VCDPA, CTDPA)</h4>
      You have the right to file an appeal based on our response to you exercising
      any of these rights. In the event you disagree with how we resolved the appeal,
      you have the right to contact the attorney general located here:

